color = input("Enter a character in VIBGYOR: ")[0]

if color in "vV":
    print("Violet")
elif color in "iI":
    print("Indigo")
elif color in "bB":
    print("Blue")
elif color in "gG":
    print("Green")
elif color in "yY":
    print("Yellow")
elif color in "oO":
    print("Orange")
elif color in "rR":
    print("Red")
else:
    print("This is not vibgyor color")
