"""
Q3: Create a list of fruits and access elements using indexing.
"""

fruits = ["Apple", "Orange", "Grape", "Kiwi", "Mango", "Banana"]

# Accessing elements using indexing
print("First element is", fruits[0])
print("Second element is", fruits[1])
print("Last element is", fruits[-1])
print("Second last element is", fruits[-2])
