age_list = [25, 24, 14, 25, 54, 56, 43, 64]

print(age_list[4])  # To access specific value using index

print(len(age_list)-1)  # To find the last index number of the list

age_list.insert(2, 34)  # To add a number in a specific place. Here first argument is index and second argument is value

age_list.insert(len(age_list)-1, 54)  # To add a value in second last position.

age_list[2] = 84  # Replacing an element using the index number

print(age_list)
