text = "helloworld"

print(text.isalpha())  # Returns True if all characters in the string are alphabetic.
print(text.isdigit())  # Returns True if all characters in the string are digits.
print()
print(text.islower())  # Returns True if all alphabetic characters in the string are lowercase.
print(text.isupper())  # Returns True if all alphabetic characters in the string are uppercase.
print()
print(text.isalnum())  # (alphanumeric characters)
print(text.isspace())  # (whitespace characters)
print(text.istitle())  # (title case)

